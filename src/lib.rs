//! Experimental Rust implementation of Mimir Bridge clients & protocols.
//! 
//! 
//! - [`mimir-proto`](../mimir_proto/index.html): core protocol implementations & helpers.
//!
//! - [`mimir-crypto`](../mimir_crypto/index.html): primitives and traits for common cryptographic
//! operations.
//!
//! - [`mimir-types`](../mimir_types/index.html): definitions and reexports of commonly used types.
//!
//! - [`mimir-util`](../mimir_util/index.html): various helper functions and macros.
//!
//! - [`mimir-worker`](../mimir_worker/index.html): core worker client logic.
//! 
//! - [`mimir-transport`](../mimir_transport/index.html): inter-client communication protocol
//! helpers.
//! 
//! - [`mimir-requester`](../mimir_requester/index.html): core requester client logic.
//! 
//! - [`mimir-node`](../mimir_node/index.html): utilities for managing and querying local
//! blockchain nodes.
//!

